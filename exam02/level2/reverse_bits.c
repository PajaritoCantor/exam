#include <stdio.h>
#include <unistd.h>

unsigned char	reverse_bits(unsigned char octet)
{
	return ((octet & 0b00000001) << 7) | ((octet & 0b00000010) << 6) | ((octet & 0b00000100) << 5) | ((octet & 0b00001000) << 4) | ((octet & 0b00010000) << 3) | ((octet & 0b00100000) << 2) | ((octet & 0b01000000) << 1) | ((octet & 0b10000000) << 0);
}