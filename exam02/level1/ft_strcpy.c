#include <stdio.h>
#include <unistd.h>

char	*ft_strcpy(char *s1, char *s2)
{
	char *ptr = s1;

	while (*s2)
	{
		*s1 = *s2;
		ptr++;
		s2++;
	}
	*ptr = '\0';
	return (s1);
}
