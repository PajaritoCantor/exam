#include <unistd.h>

int main (int ac, char **av)
{
    if (ac > 1)
    {
        int j = 0;
        while (av[j])
        {
            int i = 0;
            while (av[j][i])
            {
                if (i == 0 || av[j][i - 1] == 32 || av[j][i - 1] == 9)
                {
                    if (av[j][i] >= 97 && av[j][i] <= 122)
                        av[j][i] -= 32;
                }
                else if (av[j][i] >= 65 && av[j][i] <= 90)
                    av[j][i] += 32;
                write(1, &av[j][i], 1);
                i++;
            }
            write(1, "\n", 1); 
            j++;
        }
    }
    else
        write(1, "\n", 1);
    return 0;    
}